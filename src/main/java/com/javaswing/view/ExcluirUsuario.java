    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
            
            UsuarioControle usuarioControle = new UsuarioControle();
            Usuario usuario = new Usuario();
            
            usuario.setCodigo(Integer.parseInt(txtCodigo.getText()));
            
        try {
            usuario = usuarioControle.consultarCodigo(usuario);
        } catch (SQLException ex) {
            Logger.getLogger(ExcluirUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
            txtTitulo.setText(usuario.getNome());
            txtAutor.setText(usuario.getTelefone());
            txtEditora.setText(usuario.getCidade());
            
        
    }//GEN-LAST:event_btnConsultarActionPerformed

