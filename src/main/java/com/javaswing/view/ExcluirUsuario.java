    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
            UsuarioControle usuarioControle = new UsuarioControle();
            Usuario usuario = new Usuario();
            
            usuario.setCodigo(Integer.parseInt(txtCodigo.getText()));
            
            try {
            if(usuarioControle.excluir(usuario) == false){
                JOptionPane.showMessageDialog(null,"Usuário Excluído com Sucesso!","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
                txtCodigo.setText("");
                txtTitulo.setText("");
                txtAutor.setText("");
                txtEditora.setText("");
            }else{
                JOptionPane.showMessageDialog(null,"ERRO; Usuário não foi excluído !","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LivroCadastrarView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

